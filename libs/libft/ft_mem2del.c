#include "libft.h"

int		ft_mem2del(void ***var, size_t n)
{
	while (n--)
	{
		if (*var[n])
		{
			free((*var)[n]);
			(*var)[n] = NULL;
		}
	}
	if (*var)
	{
		free(*var);
		*var = NULL;
	}
	return (0);
}